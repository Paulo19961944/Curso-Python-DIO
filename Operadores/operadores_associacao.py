# Operadores de Associação são usados para verificar se um objeto está presente em uma sequência

curso = "Curso de Python"
frutas = ["laranja", "uva", "limao"]
saques = [1500, 100]

print("Python" in curso) # Retorna True
print("Maça" not in frutas) # Retorna True
print(200 in saques) # Retorna False